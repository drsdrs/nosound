uint16_t WAVES_RANDOM_12 [ 0x1000 ];

void waves_sine8_generate() {
    double phAmount8 = sizeof( WAVES_SINE_8 ) / sizeof( WAVES_SINE_8 [ 0 ] );
    double phInc8    = M_PI * 2 / phAmount8;
    for ( size_t i = 0; i < phAmount8; i++ ) {
        WAVES_SINE_8 [ i ] = sin( i * phInc8 );
    }
}

void waves_sine12_generate() {
    double phAmount12 = sizeof( WAVES_SINE_12 ) / sizeof( WAVES_SINE_12 [ 0 ] );
    double phInc12    = M_PI * 2 / phAmount12;
    for ( size_t i = 0; i < phAmount12; i++ ) {
        WAVES_SINE_12 [ i ] = sin( i * phInc12 );
    }
}

void waves_rnd_generate() {
    for ( size_t i = 0; i < sizeof( WAVES_RANDOM_12 ) / sizeof( WAVES_RANDOM_12 [ 0 ] ); i++ ) {
        WAVES_RANDOM_12 [ i ] = rand();
    }
}

float waves_sine12_get( uint16_t index ) {
    return WAVES_SINE_12 [ index & 0xfff ];
}
float waves_sine12_unbiased_get( uint16_t index ) {
    return ( waves_sine12_get( index ) + 1 ) / 2;
}
float waves_sine8_get( uint8_t index ) {
    return WAVES_SINE_8 [ index & 0xff ];
}
void waves_all_generate() {
    waves_sine8_generate();
    waves_sine12_generate();
    waves_rnd_generate();
}
